export class Results {
  constructor() {
    this.comparison = document.getElementById('comparison');
    this.deCard     = document.getElementById('de-recommendation');
    this.otherGrid  = document.getElementById('other-fits-grid');
    this.data       = null;

    document.getElementById('btn-cheatsheet')
      .addEventListener('click', () => this.exportCheatsheet());
  }

  render(data) {
    this.data = data;
    const [first, second, ...others] = data.distros;

    this.comparison.innerHTML = [
      this.distroCard(first, 1),
      '<div class="vs-badge">VS</div>',
      this.distroCard(second, 2),
    ].join('');

    this.deCard.innerHTML = `
      <h3>Recommended Desktop Environment</h3>
      <div class="de-info">
        <span class="de-name">${data.desktop.name}</span>
        <p class="de-desc">${data.desktop.description}</p>
        <p class="de-reason">${data.desktop.reason}</p>
      </div>`;

    this.otherGrid.innerHTML = others.map(d => this.smallCard(d)).join('');
  }

  distroCard(r, rank) {
    const pct = Math.round(r.score * 100);
    return `
      <div class="distro-card rank-${rank}">
        <div class="card-header">
          <span class="rank-badge">#${rank}</span>
          <span class="score-badge">${pct}% match</span>
        </div>
        <h3 class="distro-name">${esc(r.name)}</h3>
        <span class="distro-version">${esc(r.version)} · ${esc(r.package_manager)}</span>
        <p class="distro-tagline">${esc(r.tagline)}</p>
        <p class="distro-reason">${esc(r.reason)}</p>
        <div class="trait-badges">
          ${r.top_traits.map(t => `<span class="trait">${t.replace(/_/g, ' ')}</span>`).join('')}
        </div>
        <div class="pros-cons">
          <div class="pros">
            <h4>Pros</h4>
            <ul>${r.pros.map(p => `<li>✓ ${esc(p)}</li>`).join('')}</ul>
          </div>
          <div class="cons">
            <h4>Cons</h4>
            <ul>${r.cons.map(c => `<li>✗ ${esc(c)}</li>`).join('')}</ul>
          </div>
        </div>
        <a href="${esc(r.download_url)}" target="_blank" rel="noopener"
           class="btn-download">
          ⬇ Download ISO (${esc(r.iso_size)})
        </a>
      </div>`;
  }

  smallCard(r) {
    const pct = Math.round(r.score * 100);
    return `
      <div class="distro-card-sm">
        <div class="card-header">
          <h4>${esc(r.name)} <span class="version">${esc(r.version)}</span></h4>
          <span class="score-badge">${pct}%</span>
        </div>
        <p>${esc(r.tagline)}</p>
        <a href="${esc(r.download_url)}" target="_blank" rel="noopener"
           class="btn-download btn-sm">
          ⬇ Download (${esc(r.iso_size)})
        </a>
      </div>`;
  }

  exportCheatsheet() {
    if (!this.data) return;
    const a = this.data.distros[0];
    const b = this.data.distros[1];
    const de = this.data.desktop;

    const md = [
      `# Linux Recommendation: ${a.name} vs ${b.name}`,
      '',
      `## #1 Pick: ${a.name} ${a.version}`,
      `**Match:** ${Math.round(a.score * 100)}%`,
      `**Package manager:** ${a.package_manager}`,
      a.based_on ? `**Based on:** ${a.based_on}` : null,
      '',
      `> ${a.reason}`,
      '',
      '### Pros',
      ...a.pros.map(p => `- ✓ ${p}`),
      '',
      '### Cons',
      ...a.cons.map(c => `- ✗ ${c}`),
      '',
      `**Download:** ${a.download_url}`,
      '',
      '---',
      '',
      `## #2 Pick: ${b.name} ${b.version}`,
      `**Match:** ${Math.round(b.score * 100)}%`,
      `**Package manager:** ${b.package_manager}`,
      b.based_on ? `**Based on:** ${b.based_on}` : null,
      '',
      `> ${b.reason}`,
      '',
      '### Pros',
      ...b.pros.map(p => `- ✓ ${p}`),
      '',
      '### Cons',
      ...b.cons.map(c => `- ✗ ${c}`),
      '',
      `**Download:** ${b.download_url}`,
      '',
      '---',
      '',
      `## Desktop Environment: ${de.name}`,
      de.description,
      `> ${de.reason}`,
      '',
      '## Getting Started',
      `1. Download the ${a.name} ISO from the link above`,
      '2. Create a bootable USB with **Ventoy** (ventoy.net), **Rufus**, or **balenaEtcher**',
      '3. Boot from USB and follow the installer',
      '4. After install, run system updates',
      '5. Install your apps and enjoy Linux!',
      '',
      '---',
      '*Generated by distro-picker*',
    ].filter(l => l !== null).join('\n');

    const blob = new Blob([md], { type: 'text/markdown' });
    const link = document.createElement('a');
    link.href = URL.createObjectURL(blob);
    link.download = `linux-recommendation-${a.id}.md`;
    link.click();
    URL.revokeObjectURL(link.href);
  }
}

function esc(s) {
  const d = document.createElement('div');
  d.textContent = s;
  return d.innerHTML;
}
